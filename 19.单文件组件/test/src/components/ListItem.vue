        </label>
        <button class="btn btn-danger" @click="getDeletTodoId(todo.id)">删除</button>
    </li>
</template>

<script>
    export default {
        name:'ListItem',
        // 接收TodoList传递过来的todo数据
        props:['todo','changeCheckedStatus','deletTodo'],
